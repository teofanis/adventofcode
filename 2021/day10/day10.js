const checkLine = (line) => {
    const stack =[];
    const openBracket = ['(', '[', '{', '<'];
    const closeBracket = [')', ']', '}', '>'];
    for(let i =0; i < line.length; i++) {
        const bracket = line[i];
        if(openBracket.includes(bracket)) {
            stack.push(bracket);
        } else if(closeBracket.includes(bracket)) {
            const last = stack.pop();
            const index = closeBracket.indexOf(bracket);
            if(last !== openBracket[index]) {
                return {
                    line: line,
                    matched: false,
                    position: i,
                };
            }
        }
    }
    return {
        line: line,
        matched: true,
        missing: stack.reverse().map(bracket => closeBracket[openBracket.indexOf(bracket)]),
    };
}

const bracketCost = (bracket, part) => {
    const bracketCostMap = {
        ')': part === 1 ? 3 : 1,
        ']': part === 1 ? 57 : 2,
        '}': part === 1 ? 1197 : 3,
        '>': part === 1 ? 25137 : 4,
    };
    return bracketCostMap[bracket] ?? 0;
}

const calculateCompletingCost = (line) => {
    return line.reduce((acc, bracket) => 5* acc + bracketCost(bracket,2), 0);
}
const input = raw.trim()
    .split('\n')
    .map(line => line.split(''))
    .map(line => checkLine(line));

let result = input
    .filter((line) => !line.matched)
    .reduce((acc, line) => acc += bracketCost(line.line[line.position],1),0);

